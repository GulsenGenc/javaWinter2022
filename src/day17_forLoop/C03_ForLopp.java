package day17_forLoop;

public class C03_ForLopp {
    public static void main(String[] args) {

        //ekrana 10 kere "Java güzeldir" yazdırın.

        for (int i = 1; i <=10; i++) {
            System.out.print(" Java güzeldir");
        }

        for (int i = 0; i <=10; i++) {
            System.out.print(i+"-java güzeldir ");

        }
    }
}
